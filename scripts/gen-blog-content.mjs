// Reads content/blog/*.md and emits lib/blog-content.generated.ts — a plain JS
// module that inlines each post's frontmatter + markdown body.
//
// Why: the /blog pages are prerendered into the OpenNext incremental cache, but
// the Cloudflare waitlist deploy ships without R2 (the cache store), so those
// routes are REGENERATED inside the Worker at request time. Cloudflare Workers
// have no filesystem, so reading content/blog/*.md via node:fs at runtime finds
// nothing (empty index, 404 articles). Baking the content into a bundled module
// means the pages render correctly whether they're served from cache or
// regenerated live — no runtime fs required.
//
// Runs at the start of `npm run build` (which OpenNext invokes for cf:build /
// cf:deploy), so the generated file always matches content/. It's also committed
// so `tsc` and `next dev` work without a prior gen run.

import fs from "node:fs";
import path from "node:path";
import matter from "gray-matter";

const DIR = path.join(process.cwd(), "content", "blog");
const OUT = path.join(process.cwd(), "lib", "blog-content.generated.ts");

const files = fs.existsSync(DIR)
  ? fs.readdirSync(DIR).filter((f) => f.endsWith(".md")).sort()
  : [];

const posts = files.map((f) => {
  const slug = f.replace(/\.md$/, "");
  const { data, content } = matter(fs.readFileSync(path.join(DIR, f), "utf8"));
  return { slug, data, content };
});

const out = `// AUTO-GENERATED by scripts/gen-blog-content.mjs from content/blog/*.md.
// Do not edit by hand — run \`npm run build\` (or \`node scripts/gen-blog-content.mjs\`) to regenerate.

export interface RawPost {
  slug: string;
  data: Record<string, unknown>;
  content: string;
}

export const RAW_POSTS: RawPost[] = ${JSON.stringify(posts, null, 2)};
`;

fs.writeFileSync(OUT, out);
console.log(`gen-blog-content: wrote ${posts.length} post(s) -> ${path.relative(process.cwd(), OUT)}`);
